package com.callolr.student;

public class Ex_06 {
	
	// primitive(스칼라형, 기본형 변수)
	int num = 10;
	float num2 = 10.2f;
	double num3 = 30.0;
	long num4 = 100;
	
	// 문자는 작은 따옴표만 이용
	char char1 = 'A';
	boolean bYes = true; // true나 false값을 가지는 변수
	
	// wrapper class(primitive 변수형에 기능이 추가된 형태)
	// Integer와 Character만 형태가 다르다
	Integer num11 = 10;
	Float num22 = 20.2F;
	Double num33 = 30.0;
	Long num44 = 100L;
	
	Character char11 = 'B';
	Boolean bYes22 = true;
	
}
